/*Clases de Bootstrap en Login.js y Register.js
1. container
Clase: container
Descripción: Proporciona un contenedor centralizado con un ancho máximo predefinido y un relleno horizontal automático. En este caso, se utiliza para centrar el formulario en la pantalla.
Ubicación: <div className="container d-flex justify-content-center align-items-center vh-100">
2. d-flex
Clase: d-flex
Descripción: Aplica el display flex al contenedor, lo que permite una mejor alineación y distribución de los elementos hijos.
Ubicación: <div className="container d-flex justify-content-center align-items-center vh-100">
3. justify-content-center
Clase: justify-content-center
Descripción: Centra horizontalmente el contenido dentro del contenedor flex.
Ubicación: <div className="container d-flex justify-content-center align-items-center vh-100">
4. align-items-center
Clase: align-items-center
Descripción: Centra verticalmente el contenido dentro del contenedor flex.
Ubicación: <div className="container d-flex justify-content-center align-items-center vh-100">
5. vh-100
Clase: vh-100
Descripción: Define una altura del 100% del viewport height (altura de la ventana gráfica).
Ubicación: <div className="container d-flex justify-content-center align-items-center vh-100">
6. card
Clase: card
Descripción: Proporciona un diseño de tarjeta con bordes redondeados y sombra, comúnmente utilizado para formularios y contenedores de contenido.
Ubicación: <div className="card p-4" style={{ maxWidth: '400px', width: '100%' }}>
7. p-4
Clase: p-4
Descripción: Agrega un relleno (padding) de 1.5rem (24px) alrededor de la tarjeta para un espaciado interno.
Ubicación: <div className="card p-4" style={{ maxWidth: '400px', width: '100%' }}>
8. card-title
Clase: card-title
Descripción: Estiliza el título de la tarjeta con un tamaño de fuente y un margen adecuados.
Ubicación: <h2 className="card-title text-center">Login</h2>
9. text-center
Clase: text-center
Descripción: Centra el texto horizontalmente.
Ubicación: <h2 className="card-title text-center">Login</h2>
10. form-group
Clase: form-group
Descripción: Proporciona un margen inferior a los elementos de formulario para separarlos visualmente.
Ubicación: <div className="mb-3">
11. form-label
Clase: form-label
Descripción: Estiliza las etiquetas del formulario.
Ubicación: <label htmlFor="username" className="form-label">Username</label>
12. form-control
Clase: form-control
Descripción: Estiliza los campos de entrada del formulario, proporcionando un aspecto uniforme y adecuado.
Ubicación: <input type="text" className="form-control" id="username" value={username} onChange={(e) => setUsername(e.target.value)} required />
13. mb-3
Clase: mb-3
Descripción: Agrega un margen inferior de 1rem (16px) a los elementos del formulario para un mejor espaciado.
Ubicación: <div className="mb-3">
14. btn
Clase: btn
Descripción: Aplica estilos básicos de botón.
Ubicación: <button type="submit" className="btn btn-primary w-100">Login</button>
15. btn-primary
Clase: btn-primary
Descripción: Aplica estilos específicos para un botón de tipo primario, generalmente con un color de fondo azul.
Ubicación: <button type="submit" className="btn btn-primary w-100">Login</button>
16. w-100
Clase: w-100
Descripción: Establece el ancho del elemento al 100% de su contenedor.
